#!/usr/bin/env python3
"""
build.py — paper-review-skills の単一ソース・ジェネレータ

正 = ~/.claude/skills/<subset>。このスクリプトが repo の
  - skills/<name>/        （SKILL.md + references を複製）
  - prompts/<name>.md     （frontmatter除去・references inline した汎用プロンプト）
  - prompts/research-review.md （meta を flatten した総合プロンプト）
  - prompts/README.md
  - install.sh の SKILLS 配列（sentinel間）
を再生成する。手編集禁止（生成物には GENERATED バナー）。

使い方:  python3 build/build.py [--claude-dir ~/.claude/skills]
再実行で差分が出なければ冪等。auto-commit はしない。
"""
import os, re, sys, shutil, argparse

# --- 対象サブセット（唯一の一覧）。役割はプロンプト見出し用 ---
SUBSET = [
    "research-review",   # meta（flatten対象。standalone prompt は作らず research-review.md=flatten を作る）
    "framing-check", "aim-check", "articulation-check", "weakness-analyze", "lit-survey",
    "format-check",
    "shepherd", "student-comment", "review-tracker",
]
# meta 本文はレンズ論理を持たない（委譲専用）ので、standalone prompt は生成しない。
LENSES_FOR_STANDALONE = [s for s in SUBSET if s != "research-review"]
# repo に残っていてはいけない旧名 dir（リネーム/統合で消えたもの）
STALE_DIRS = ["review", "thesis-review", "check-articulation",
              "review-analyze", "review-shepherd", "survey-literature", "abstract-format-check", "abstract-review"]

BANNER = "<!-- GENERATED by build/build.py from ~/.claude/skills — 手編集しないこと。編集は ~/.claude/skills 側で。 -->"
ARG_REPLACEMENT = "このプロンプトの末尾に貼り付けられた文書（またはトピック）"

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

def read(p):
    with open(p, encoding="utf-8") as f:
        return f.read()

def write(p, s):
    os.makedirs(os.path.dirname(p), exist_ok=True)
    with open(p, "w", encoding="utf-8") as f:
        f.write(s)

def split_frontmatter(text):
    """先頭 --- ... --- を除去し (frontmatter_dict_partial, body) を返す。"""
    m = re.match(r"^---\n(.*?)\n---\n(.*)$", text, re.DOTALL)
    if not m:
        return {}, text
    fm_raw, body = m.group(1), m.group(2)
    fm = {}
    for line in fm_raw.splitlines():
        mm = re.match(r"^(name|description|argument-hint):\s*(.*)$", line)
        if mm:
            fm[mm.group(1)] = mm.group(2).strip().strip('"')
    return fm, body.lstrip("\n")

def inline_references(skill_dir, body):
    """references/*.md があれば末尾に appendix として inline する。"""
    ref_dir = os.path.join(skill_dir, "references")
    if not os.path.isdir(ref_dir):
        return body
    refs = sorted(f for f in os.listdir(ref_dir) if f.endswith(".md"))
    if not refs:
        return body
    parts = [body.rstrip(),
             "\n\n---\n\n## 参照資料（本文中で `references/…` として参照されるファイルの内容をここに展開）\n"]
    for r in refs:
        parts.append(f"\n### `references/{r}`\n\n" + read(os.path.join(ref_dir, r)).rstrip() + "\n")
    return "\n".join(parts)

def render_body(claude_dir, name):
    """frontmatter除去・$ARGUMENTS置換・references inline した本文を返す。"""
    skill_dir = os.path.join(claude_dir, name)
    _, body = split_frontmatter(read(os.path.join(skill_dir, "SKILL.md")))
    body = body.replace("$ARGUMENTS", ARG_REPLACEMENT)
    body = inline_references(skill_dir, body)
    return body

def title_of(desc, name):
    return name

PREAMBLE_NOTE = (
    "> このプロンプトは任意のLLM（ChatGPT / Gemini / Claude 等）にそのまま貼って使えます。\n"
    "> **すべて1回の返信で出力**してください。本文中に「ファイルに書き出す」「シェルを実行」「日付を取得」等の\n"
    "> 指示があっても無視し、結果はこの会話にテキストとして返してください。\n"
)
FOOTER = (
    "\n\n---\n\n## ここに文書を貼る / Paste your document below\n"
    "この行より下をすべて置き換えて、対象の文書（.tex/.md 本文やテキスト）を貼り付け、送信してください。\n\n"
    "[PASTE YOUR DOCUMENT HERE]\n"
)

def make_prompt(claude_dir, name):
    fm, _ = split_frontmatter(read(os.path.join(claude_dir, name, "SKILL.md")))
    desc = fm.get("description", "").split("。")[0]
    body = render_body(claude_dir, name)
    return (f"{BANNER}\n\n# {name} — portable prompt\n\n{desc}。\n\n{PREAMBLE_NOTE}\n"
            f"---\n\n## 指示 / Instructions\n\n{body}\n{FOOTER}")

def make_meta_prompt(claude_dir):
    tmpl = read(os.path.join(REPO, "build", "templates", "research-review.prompt.tmpl"))
    def repl(m):
        key = m.group(1).strip()
        if key == "format-check-condensed":
            return read(os.path.join(REPO, "build", "templates", "format-check-condensed.md")).rstrip()
        return render_body(claude_dir, key).rstrip()
    out = re.sub(r"\{\{include-body:\s*([^}]+)\}\}", repl, tmpl)
    return f"{BANNER}\n\n{out.rstrip()}\n{FOOTER}"

def regen_skills(claude_dir):
    dst = os.path.join(REPO, "skills")
    if os.path.isdir(dst):
        shutil.rmtree(dst)
    os.makedirs(dst)
    for name in SUBSET:
        shutil.copytree(os.path.join(claude_dir, name), os.path.join(dst, name))
    # 念のため旧名dirが repo 直下 skills に残っていないことを保証（rmtree済みなので自明だが明示）

def regen_prompts(claude_dir):
    dst = os.path.join(REPO, "prompts")
    if os.path.isdir(dst):
        shutil.rmtree(dst)
    os.makedirs(dst)
    write(os.path.join(dst, "research-review.md"), make_meta_prompt(claude_dir))
    for name in LENSES_FOR_STANDALONE:
        write(os.path.join(dst, f"{name}.md"), make_prompt(claude_dir, name))
    write(os.path.join(dst, "README.md"), PROMPTS_README)

def regen_install_skills_array():
    p = os.path.join(REPO, "install.sh")
    s = read(p)
    arr = "\n".join(f'    "{n}"' for n in SUBSET)
    block = f"# BEGIN GENERATED:skills (build/build.py)\nSKILLS=(\n{arr}\n)\n# END GENERATED:skills"
    s2 = re.sub(r"# BEGIN GENERATED:skills.*?# END GENERATED:skills", block, s, flags=re.DOTALL)
    if s2 == s and "BEGIN GENERATED:skills" not in s:
        # sentinel未設置なら既存 SKILLS=(...) を置換して sentinel を導入
        s2 = re.sub(r"SKILLS=\(\n.*?\n\)", block, s, flags=re.DOTALL)
    write(p, s2)

PROMPTS_README = f"""{BANNER}

# prompts/ — Claude Code なしで使う（任意のLLMに貼るだけ）

Claude Code を使わない人（ChatGPT / Gemini / Copilot / ローカルLLM 等）向けの、コピペで動くプロンプト集です。

## 使い方
1. 使いたいファイルを**丸ごとコピー**する。
2. 新しいチャットに貼る。
3. 末尾の「ここに文書を貼る」以下を、レビューしたい概要・論文・卒論の本文に置き換えて送信。

## まずこれ
- **`research-review.md`** — 総合レビュー（教員向け）。骨子・一貫性・当てどころ・関連研究・説明不足・弱点・体裁を一気に見て、提出前の最優先手つきで返す。概要/論文/卒論を自動で見分けます。
- **`student-comment.md`** — 上の教員向けレビュー出力を貼ると、学生に渡せる前向き・優先順の1枚に翻訳します（教員→学生の2層）。

## 単体レンズ（1観点だけ見たいとき）
`framing-check`（骨子＋旗と物差しの一貫性）/ `aim-check`（当てどころ・框の付け替え）/ `articulation-check`（説明の抜け5W）/ `weakness-analyze`（弱点）/ `lit-survey`（関連研究）/ `format-check`（体裁・概要は紙面配分）/ `shepherd`（分析→前向き改善）/ `review-tracker`（複数レビュー統合）。

> 注: これらは `~/.claude/skills/` のスキル本文から自動生成しています（`build/build.py`）。中身の編集はスキル側で。
"""

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--claude-dir", default=os.path.expanduser("~/.claude/skills"))
    args = ap.parse_args()
    cd = os.path.expanduser(args.claude_dir)

    missing = [n for n in SUBSET if not os.path.isfile(os.path.join(cd, n, "SKILL.md"))]
    if missing:
        sys.exit(f"ERROR: source skills not found under {cd}: {', '.join(missing)}")

    regen_skills(cd)
    regen_prompts(cd)
    regen_install_skills_array()

    # 旧名dirが repo 直下に残っていたら警告（skills/ は再生成済みなので通常出ない）
    leftover = [d for d in STALE_DIRS if os.path.isdir(os.path.join(REPO, "skills", d))]
    if leftover:
        print("WARNING: stale skill dirs still present:", leftover)

    print(f"OK: {len(SUBSET)} skills mirrored, prompts regenerated, install.sh SKILLS updated.")
    print("Next: review `git status` / `git diff`, then commit & push manually.")

if __name__ == "__main__":
    main()
